
using Microsoft.AspNetCore.Identity.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore;
using UsuariosAlmaNetCoreMVC.Data;
using Microsoft.AspNetCore.Identity;
using UsuariosAlmaNetCoreMVC.Interfaces;
using UsuariosAlmaNetCoreMVC.Services;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.IdentityModel.Tokens;
using System.Text;


var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
builder.Services.AddControllersWithViews().AddRazorRuntimeCompilation();
builder.Services.AddDbContext<ApplicationDbContext>(options =>
    options.UseSqlServer(builder.Configuration.GetConnectionString("DefaultConnection")));

builder.Services.AddIdentity<IdentityUser, IdentityRole>(options =>
{
    options.Password.RequireDigit = true;
    options.Password.RequireLowercase = true;
    options.Password.RequireNonAlphanumeric = true;
    options.Password.RequireUppercase = true;
    options.Password.RequiredLength = 6;
})
.AddEntityFrameworkStores<ApplicationDbContext>()
.AddDefaultTokenProviders();

builder.Services.AddTransient<IEmailService>(provider =>
    new EmailService(
        builder.Configuration["EmailSettings:Host"],
        int.Parse(builder.Configuration["EmailSettings:Port"]),
        builder.Configuration["EmailSettings:Username"],
        builder.Configuration["EmailSettings:Password"]
    ));

